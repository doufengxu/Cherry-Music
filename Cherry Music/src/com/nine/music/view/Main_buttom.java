package com.nine.music.view;

import android.content.Context;
import android.util.AttributeSet;
import android.view.LayoutInflater;
import android.widget.FrameLayout;
import android.widget.ImageView;
import android.widget.LinearLayout;
import android.widget.TextView;

import com.nine.music.R;

public class Main_buttom extends FrameLayout {

	public LinearLayout linearLayout_left;
	public LinearLayout linearLayout_right;
	public TextView local_textview;
	public TextView find_textview;
	public ImageView local_pic;
	public ImageView find_pic;

	public Main_buttom(Context context, AttributeSet attrs) {
		super(context, attrs);
		LayoutInflater.from(context).inflate(R.layout.main_buttom, this);
		linearLayout_left = (LinearLayout) findViewById(R.id.linearLayout_left);
		linearLayout_right = (LinearLayout) findViewById(R.id.linearLayout_right);
		local_textview = (TextView) findViewById(R.id.textView_left);
		find_textview = (TextView) findViewById(R.id.textView_right);
		local_pic=(ImageView) findViewById(R.id.imageView_left);
		find_pic=(ImageView) findViewById(R.id.imageView_right);
	}

}
